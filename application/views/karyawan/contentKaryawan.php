    function tambah() {
    var nik = $("#nik").val()
    var namaLengkap = $("#namaLengkap").val()
    var jenisKelamin = $("#jenisKelamin").val()
    var tempatLahir = $("#tempatLahir").val()
    var alamat = $("#alamat").val()
    var tanggalLahir = $("#tanggalLahir").val()
    //Split tanggal
    var split_tgl = tanggalLahir.split("/");
    var tahun = split_tgl[2];
    var bulan = split_tgl[1];
    var hari = split_tgl[0];
    var tgl = tahun+"-"+bulan+"-"+hari;

    var comboDepartement = $("#comboDepartement").val()
    var tanggalMasuk = $("#tanggalMasuk").val()
    var comboStatus = $("#combostatus").val()
    var tanggalMulai = $("#tanggalMulai").val()
    var tanggalHabis = $("#tanggalHabis").val()
        $.ajax({
        type: 'GET',
        url: '<?php echo base_url() ?>Karyawan/tambah',
        data: '&nik=' + nik + '&namaLengkap=' + namaLengkap + '&jenisKelamin=' + jenisKelamin + '&tempatLahir=' + tempatLahir +
        '&tanggalLahir=' + tgl+ '&alamat=' + alamat+ '&comboDepartement=' + comboDepartement+ '&tanggalMasuk=' + tanggalMasuk +
        '&comboStatus=' + comboStatus + '&tanggalMulai=' + tanggalMulai + '&tanggalHabis=' + tanggalHabis,
        success: function (html) {
            swal({
                title: "Yeah",
                text: "Tambah Data Sukses !",
                type: "success",
                confirmButtonClass: 'btn-success',
                confirmButtonText: 'Ok',
            },
            function (isConfirm) {
                if(isConfirm){
                    window.location.reload();
                }
            });
        }
    });
    }

    function readURL(input) {
    if (input.files && input.files[0]) {
        var reader = new FileReader();
        
        reader.onload = function (e) {
        $('#prev_foto').attr('src', e.target.result);
        }
        
        reader.readAsDataURL(input.files[0]);
    }
    } 

    $(document).ready(function(){
        $('#file_gambar').change(function(){
            readURL(this);
        });
    });
</script>